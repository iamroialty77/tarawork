"use client";

import { useState, useEffect } from "react";
import { supabase } from "../lib/supabase";
import { motion, AnimatePresence } from "framer-motion";
import { 
  Mail, 
  Lock, 
  User, 
  ArrowRight, 
  Loader2, 
  Github, 
  Facebook,
  Linkedin,
  AlertCircle,
  CheckCircle2,
  Zap,
  PartyPopper,
  Briefcase,
  Users
} from "lucide-react";
import { cn } from "../lib/utils";
import { useSearchParams } from "next/navigation";

export default function AuthForm() {
  const searchParams = useSearchParams();
  const confirmed = searchParams.get('confirmed');
  
  const [mode, setMode] = useState<"login" | "signup">("login");
  const [loading, setLoading] = useState(false);
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [fullName, setFullName] = useState("");
  const [role, setRole] = useState<"jobseeker" | "hirer">("jobseeker");
  const [error, setError] = useState<string | null>(null);
  const [success, setSuccess] = useState<string | null>(null);

  useEffect(() => {
    if (confirmed === 'true') {
      setSuccess("Email confirmed successfully! You can now log in.");
      setMode("login");
    }
  }, [confirmed]);

  const handleSocialLogin = async (provider: 'google' | 'facebook' | 'linkedin' | 'github') => {
    setLoading(true);
    setError(null);
    try {
      // Set a flag to show notification on redirect back
      sessionStorage.setItem('social_login_pending', provider);
      
      // Use linkedin_oidc for newer projects as it's the standard now
      const effectiveProvider = provider === 'linkedin' ? 'linkedin_oidc' : provider;
      
      const { error } = await supabase.auth.signInWithOAuth({
        provider: effectiveProvider as any,
        options: {
          redirectTo: `${window.location.origin}/auth/callback`,
        },
      });
      if (error) throw error;
    } catch (err: any) {
      console.error("Social login error:", err);
      let message = err.message || "An error occurred during social login.";
      
      if (message.includes("provider is not enabled")) {
        message = `Authentication Error: Ang ${provider} login ay hindi pa enabled sa Supabase Dashboard. Pakipuntahan ang Authentication > Providers at i-enable ang ${provider}.`;
      }
      
      setError(message);
      setLoading(false);
    }
  };

  const handleAuth = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError(null);
    setSuccess(null);

    try {
      if (mode === "signup") {
        const { error } = await supabase.auth.signUp({
          email,
          password,
          options: {
            data: {
              full_name: fullName,
              role: role,
            },
            emailRedirectTo: `${window.location.origin}/auth/callback`,
          },
        });
        if (error) throw error;
        setSuccess("Registration successful! Please check your email to confirm your account.");
      } else {
        const { error } = await supabase.auth.signInWithPassword({
          email,
          password,
        });
        if (error) throw error;
        setSuccess("Logged in successfully!");
        // Redirect or refresh state here if needed
        window.location.href = "/";
      }
    } catch (err: any) {
      setError(err.message || "An error occurred during authentication.");
    } finally {
      setLoading(false);
    }
  };

  return (
    <div className="w-full max-w-md mx-auto">
      <motion.div 
        initial={{ opacity: 0, y: 20 }}
        animate={{ opacity: 1, y: 0 }}
        className="bg-white rounded-xl shadow-2xl shadow-indigo-100/20 border border-slate-200 overflow-hidden"
      >
        <div className="p-8">
          <div className="flex justify-center mb-8">
            <img 
              src="/tarawork-removebg-preview.png" 
              alt="Tara Logo" 
              className="h-12 w-auto object-contain"
            />
          </div>

          <div className="text-center mb-8">
            <h2 className="text-2xl font-extrabold text-slate-900">
              {mode === "login" ? "Welcome back" : "Create an account"}
            </h2>
            <p className="text-slate-500 mt-2 text-sm font-medium">
              {mode === "login" 
                ? "Enter your details to access your account." 
                : "Join thousands of freelancers and clients today."}
            </p>
          </div>

          <form onSubmit={handleAuth} className="space-y-4">
            <AnimatePresence mode="wait">
              {mode === "signup" && (
                <motion.div
                  key="signup-fields"
                  initial={{ opacity: 0, height: 0 }}
                  animate={{ opacity: 1, height: "auto" }}
                  exit={{ opacity: 0, height: 0 }}
                  className="space-y-4 mb-4"
                >
                  <div>
                    <label className="block text-xs font-black text-slate-400 uppercase tracking-widest mb-1.5 ml-1">Account Type</label>
                    <div className="grid grid-cols-2 gap-3">
                      <button
                        type="button"
                        onClick={() => setRole("jobseeker")}
                        className={cn(
                          "flex flex-col items-center justify-center p-4 rounded-xl border-2 transition-all gap-2",
                          role === "jobseeker" 
                            ? "border-indigo-600 bg-indigo-50/30 text-indigo-600" 
                            : "border-slate-100 bg-slate-50 text-slate-400 hover:border-slate-200"
                        )}
                      >
                        <Briefcase className={cn("w-6 h-6", role === "jobseeker" ? "text-indigo-600" : "text-slate-400")} />
                        <span className="text-xs font-bold uppercase tracking-wider">Job Seeker</span>
                      </button>
                      <button
                        type="button"
                        onClick={() => setRole("hirer")}
                        className={cn(
                          "flex flex-col items-center justify-center p-4 rounded-xl border-2 transition-all gap-2",
                          role === "hirer" 
                            ? "border-indigo-600 bg-indigo-50/30 text-indigo-600" 
                            : "border-slate-100 bg-slate-50 text-slate-400 hover:border-slate-200"
                        )}
                      >
                        <Users className={cn("w-6 h-6", role === "hirer" ? "text-indigo-600" : "text-slate-400")} />
                        <span className="text-xs font-bold uppercase tracking-wider">Hirer</span>
                      </button>
                    </div>
                  </div>

                  <div>
                    <label className="block text-xs font-black text-slate-400 uppercase tracking-widest mb-1.5 ml-1">Full Name</label>
                    <div className="relative group">
                      <User className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-slate-400 group-focus-within:text-indigo-600 transition-colors" />
                      <input
                        type="text"
                        placeholder="Juan Dela Cruz"
                        required
                        className="w-full bg-slate-50 border border-slate-200 rounded-xl py-3.5 pl-12 pr-4 text-sm font-medium focus:outline-none focus:ring-2 focus:ring-indigo-600/10 focus:border-indigo-600 transition-all text-slate-900"
                        value={fullName}
                        onChange={(e) => setFullName(e.target.value)}
                      />
                    </div>
                  </div>
                </motion.div>
              )}
            </AnimatePresence>

            <div>
              <label className="block text-xs font-black text-slate-400 uppercase tracking-widest mb-1.5 ml-1">Email Address</label>
              <div className="relative group">
                <Mail className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-slate-400 group-focus-within:text-indigo-600 transition-colors" />
                <input
                  type="email"
                  placeholder="juan@example.com"
                  required
                  className="w-full bg-slate-50 border border-slate-200 rounded-xl py-3.5 pl-12 pr-4 text-sm font-medium focus:outline-none focus:ring-2 focus:ring-indigo-600/10 focus:border-indigo-600 transition-all text-slate-900"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                />
              </div>
            </div>

            <div>
              <div className="flex justify-between items-center mb-1.5 ml-1">
                <label className="block text-xs font-black text-slate-400 uppercase tracking-widest">Password</label>
                {mode === "login" && (
                  <button type="button" className="text-[10px] font-bold text-indigo-600 hover:text-indigo-700">Forgot password?</button>
                )}
              </div>
              <div className="relative group">
                <Lock className="absolute left-4 top-1/2 -translate-y-1/2 w-5 h-5 text-slate-400 group-focus-within:text-indigo-600 transition-colors" />
                <input
                  type="password"
                  placeholder="••••••••"
                  required
                  className="w-full bg-slate-50 border border-slate-200 rounded-xl py-3.5 pl-12 pr-4 text-sm font-medium focus:outline-none focus:ring-2 focus:ring-indigo-600/10 focus:border-indigo-600 transition-all text-slate-900"
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                />
              </div>
            </div>

            {error && (
              <motion.div 
                initial={{ opacity: 0, x: -10 }}
                animate={{ opacity: 1, x: 0 }}
                className="flex items-center gap-2 p-3 bg-red-50 border border-red-100 rounded-xl text-red-600 text-xs font-bold"
              >
                <AlertCircle className="w-4 h-4 shrink-0" />
                {error}
              </motion.div>
            )}

            {success && (
              <motion.div 
                initial={{ opacity: 0, x: -10 }}
                animate={{ opacity: 1, x: 0 }}
                className="flex items-center gap-2 p-3 bg-emerald-50 border border-emerald-100 rounded-xl text-emerald-600 text-xs font-bold"
              >
                <CheckCircle2 className="w-4 h-4 shrink-0" />
                {success}
              </motion.div>
            )}

            <button
              type="submit"
              disabled={loading}
              className="w-full bg-slate-900 hover:bg-black text-white font-bold py-4 rounded-xl shadow-lg shadow-slate-200 transition-all flex items-center justify-center gap-2 disabled:opacity-70 group"
            >
              {loading ? (
                <Loader2 className="w-5 h-5 animate-spin" />
              ) : (
                <>
                  {mode === "login" ? "Sign In" : "Get Started"}
                  <ArrowRight className="w-4 h-4 group-hover:translate-x-1 transition-transform" />
                </>
              )}
            </button>
          </form>

          <div className="relative my-8">
            <div className="absolute inset-0 flex items-center">
              <div className="w-full border-t border-slate-100"></div>
            </div>
            <div className="relative flex justify-center text-xs uppercase font-bold tracking-widest">
              <span className="bg-white px-4 text-slate-400">Or continue with</span>
            </div>
          </div>

          <div className="grid grid-cols-2 gap-3">
            <button 
              type="button"
              onClick={() => handleSocialLogin('google')}
              className="flex items-center justify-center gap-3 w-full bg-white border border-slate-200 hover:bg-slate-50 py-3 rounded-xl text-xs font-bold text-slate-600 uppercase tracking-wider transition-all"
            >
              <svg className="w-5 h-5" viewBox="0 0 24 24">
                <path
                  d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z"
                  fill="#4285F4"
                />
                <path
                  d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z"
                  fill="#34A853"
                />
                <path
                  d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l3.66-2.84z"
                  fill="#FBBC05"
                />
                <path
                  d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z"
                  fill="#EA4335"
                />
              </svg>
              Google
            </button>
            <button 
              type="button"
              onClick={() => handleSocialLogin('github')}
              className="flex items-center justify-center gap-3 w-full bg-white border border-slate-200 hover:bg-slate-50 py-3 rounded-xl text-xs font-bold text-slate-600 uppercase tracking-wider transition-all"
            >
              <Github className="w-5 h-5" />
              GitHub
            </button>
            <button 
              type="button"
              onClick={() => handleSocialLogin('facebook')}
              className="flex items-center justify-center gap-3 w-full bg-white border border-slate-200 hover:bg-slate-50 py-3 rounded-xl text-xs font-bold text-slate-600 uppercase tracking-wider transition-all"
            >
              <Facebook className="w-5 h-5 text-[#1877F2]" />
              Facebook
            </button>
            <button 
              type="button"
              onClick={() => handleSocialLogin('linkedin')}
              className="flex items-center justify-center gap-3 w-full bg-white border border-slate-200 hover:bg-slate-50 py-3 rounded-xl text-xs font-bold text-slate-600 uppercase tracking-wider transition-all"
            >
              <Linkedin className="w-5 h-5 text-[#0A66C2]" />
              LinkedIn
            </button>
          </div>
        </div>

        <div className="p-6 bg-slate-50 border-t border-slate-100 text-center">
          <p className="text-sm font-medium text-slate-500">
            {mode === "login" ? "Don't have an account?" : "Already have an account?"}{" "}
            <button
              onClick={() => setMode(mode === "login" ? "signup" : "login")}
              className="text-indigo-600 font-bold hover:underline"
            >
              {mode === "login" ? "Create one now" : "Sign in instead"}
            </button>
          </p>
        </div>
      </motion.div>
    </div>
  );
}
